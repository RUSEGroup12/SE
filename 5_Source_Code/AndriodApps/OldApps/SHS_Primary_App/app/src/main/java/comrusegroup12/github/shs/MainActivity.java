    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {

            switch (position){

                case 0:
                    Fragment_HVAC tab1 = new Fragment_HVAC();
                    return tab1;
                case 1:
                    Fragment_Audio tab2 = new Fragment_Audio();
                    return tab2;
                case 2:
                    Fragment_Schedule tab3 = new Fragment_Schedule();
                    return tab3;
                default:
                    return null;
            }

        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "HVAC";
                case 1:
                    return "Audio";
                case 2:
                    return "Schedule";
            }
            return null;
        }
    }



